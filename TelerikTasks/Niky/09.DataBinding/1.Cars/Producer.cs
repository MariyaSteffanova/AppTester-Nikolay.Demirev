using System;
using System.Collections.Generic;

public class Producer
{
    private string name;
    private List<Model> models;

    public Producer(string name, List<Model> models)
    {
        this.name = name;
        this.models = models;

    }

    public string Name
    {
        get
        {
            return this.name;
        }
        set
        {
            this.name = value;
        }
    }

    public List<Model> Models
    {
        get
        {
            return this.models;
        }
        set
        {
            this.models = value;
        }
    }
}